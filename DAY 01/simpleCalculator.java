// Challenge 1: Simple Calculator

// Task: Write a Java program that:
// Declares two integer variables.
// Adds, subtracts, multiplies, and divides these two numbers.
// Prints the results of each operation.

public class simpleCalculator { 
    public static void main(String[] args) {
        int a=20;
        int b=40;

        System.out.println("Sum:"+(a+b));
        System.out.println("Subtract:"+(a-b));
        System.out.println("Multiplies:"+(a*b));
        System.out.println("Divide:"+(b/a));
    }
}

